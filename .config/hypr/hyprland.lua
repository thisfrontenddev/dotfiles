-- ── Workspace → monitor (AeroSpace workspace-to-monitor-force-assignment) ──
-- DP-5   = Gigabyte MO27Q28G (OLED) — this was "main"/"MO27Q28G" on the Mac.
-- HDMI-A-2 = Lenovo G27c-10  (IPS)  — this was "secondary" on the Mac.
-- The Mac had three monitor identities (main / secondary / MO27Q28G); they
-- collapse onto two screens here, so "main" and "MO27Q28G" both land on the OLED.
-- To move a workspace to the other screen, just change its monitor below.
local OLED = "DP-5"
local IPS  = "HDMI-A-2"
local wsMonitor = {
    ["1"] = OLED, ["7"] = OLED, ["8"] = OLED, ["9"] = OLED, ["10"] = OLED,
    ["2"] = IPS,  ["3"] = IPS,  ["4"] = IPS,  ["5"] = IPS,  ["6"]  = IPS,
    ["name:C"] = OLED, ["name:W"] = OLED, ["name:M"] = OLED,
    -- F, L, N, P, S, T were unpinned on the Mac → they open on the focused monitor
}
for ws, mon in pairs(wsMonitor) do
    hl.workspace_rule({ workspace = ws, monitor = mon })
end

-- ── App → workspace (AeroSpace on-window-detected) ──
-- The declarative `hl.window_rule{match=…}` API doesn't route windows on this
-- 0.56 build, so this mirrors AeroSpace's on-window-detected directly: on every
-- window open, look up its class and move it to the mnemonic workspace.
--   ws     = target workspace ("name:X" for letters, or a number as a string)
--   follow = true  → jump to that workspace with the window (--focus-follows-window)
--            false → move it there silently, leaving your view where it is
-- Class is the Wayland app class; find it with `hyprctl clients | grep class`.
local appWorkspace = {
    ["firefox"]               = { ws = "name:W", follow = true  }, -- browser (was Arc/Safari)
    ["Cursor"]                = { ws = "name:C", follow = true  }, -- editor
    ["com.mitchellh.ghostty"] = { ws = "name:T", follow = true  }, -- terminal
    ["obsidian"]              = { ws = "name:N", follow = true  }, -- notes
    ["Slack"]                 = { ws = "name:S", follow = true  }, -- chat
    -- Apps below have no native Linux build / unknown class yet — open the app,
    -- grab its class from `hyprctl clients`, drop it in on the left, and uncomment:
    -- ["Linear"]             = { ws = "name:P", follow = true  }, -- Linear
    -- ["<calendar class>"]   = { ws = "9",      follow = false }, -- calendar (was Cron)
    -- ["<music class>"]      = { ws = "8",      follow = false }, -- music (was Apple Music)
    -- ["<claude class>"]     = { ws = "10",     follow = true  }, -- Claude desktop
}

-- win is an HL.Window userdata (not a plain table), and it isn't focused yet at
-- open time — so match on win.class and move that exact window by its address.
hl.on("window.open", function(win)
    if not win then return end
    local rule = appWorkspace[win.class] or appWorkspace[win.initial_class]
    if not rule then return end
    hl.dispatch(hl.dsp.window.move({
        workspace = rule.ws,
        window    = "address:" .. win.address,
        silent    = not rule.follow,
    }))
end)

-- Finder floated on the Mac → float the file manager (thunar) when it opens.
hl.on("window.open", function(win)
    if win and (win.class or ""):match("[Tt]hunar") then
        hl.dispatch(hl.dsp.window.float({ action = "enable", window = "address:" .. win.address }))
    end
end)

