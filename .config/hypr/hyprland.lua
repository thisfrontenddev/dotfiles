-- Upstream is hyprlang-only and frozen at Hyprland 0.54 (see its readme), so the
-- palette is parsed out of its vars.conf here and theme.conf is mirrored in Lua
-- below. Palette updates still flow in via: git -C <cache> pull

local function parse_vars(path)
    local vars = {}
    local f = io.open(path, "r")
    if not f then return vars end
    for line in f:lines() do
        local k, v = line:match("^%s*%$([%w_]+)%s*=%s*(.-)%s*$")
        if k then
            vars[k] = (v:gsub("%s*#.*$", ""))
        end
    end
    f:close()
    return vars
end

