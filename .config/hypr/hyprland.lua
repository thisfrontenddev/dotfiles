-- ── hyprbars: titlebars ──
-- Plugin config keys/functions only exist while the plugin is loaded, and
-- hl.plugin.load() is async — so the plugin is loaded by load-hyprbars.sh
-- (autostart above), which then re-runs `hyprctl reload` to land back here
-- with hyprbars available. On a plain reload it's already loaded.
local barsLoaded = false
for _, p in ipairs(hl.get_loaded_plugins() or {}) do
    if p.name == "hyprbars" then barsLoaded = true end
end

if barsLoaded then
    hl.config({
        plugin = {
            hyprbars = {
                bar_height = 28,
                bar_color  = "rgb(" .. V.no0 .. ")", -- CYBR background
                col        = { text = "rgb(" .. V.wh0 .. ")" }, -- CYBR foreground
                bar_text_font              = "GeistMono NF",
                bar_text_size              = 14,
                bar_text_align             = "left",
                bar_buttons_alignment      = "left",
                bar_padding                = 16, -- horizontal inset (leading offset of the button group)
                bar_button_padding         = 8,  -- spacing between the buttons
                bar_part_of_window         = true,
                bar_precedence_over_border = true,
            },
        },
    })

    -- buttons render right-to-left: close (red), minimize (yellow), maximize (green)
    local function bar_button(color, action)
        hl.plugin.hyprbars.add_button({
            bg_color = "rgb(" .. color .. ")",
            fg_color = "rgb(FFFFFF)", -- unused: no icon glyphs
            size     = 13,
            icon     = "",
            action   = action,
        })
    end
    bar_button(V.re0, "hyprctl dispatch \"hl.dsp.window.close()\"")
    -- minimize/restore toggle: stash in special:minimized, or (if already there)
    -- send the window back to its origin workspace. See scripts/minimize-toggle.sh
    bar_button(V.ye0, scripts .. "/minimize-toggle.sh")
    -- maximize: fill the workspace (mode=1 keeps gaps/bar; not true fullscreen)
    bar_button(V.gr0, "hyprctl dispatch \"hl.dsp.window.fullscreen({mode=1})\"")
end
