-- ── Workspaces ──
-- Ported from the macOS AeroSpace config: numbered + letter-named workspaces,
-- each pinned to a monitor, with apps auto-routed to their workspace. The two
-- rule sections that follow do the monitor pinning and the app assignment.

-- ALT + {n}: switch to workspace n; ALT + SHIFT + {n}: move focused window there
for i = 1, 10 do
    local key = i % 10 -- 10 maps to key 0
    hl.bind("ALT + " .. key, hl.dsp.focus({ workspace = i }))
    hl.bind("ALT + SHIFT + " .. key, hl.dsp.window.move({ workspace = i }))
end

-- ALT + {letter}: the mnemonic workspaces (W=web, C=code, T=term, S=slack, …)
local namedWorkspaces = { "C", "F", "L", "M", "N", "P", "S", "T", "W" }
for _, ws in ipairs(namedWorkspaces) do
    hl.bind("ALT + " .. ws, hl.dsp.focus({ workspace = "name:" .. ws }))
    hl.bind("ALT + SHIFT + " .. ws, hl.dsp.window.move({ workspace = "name:" .. ws }))
end

-- i3-style directional focus / move (AeroSpace's ctrl-alt-hjkl). h/j/k/l = ←/↓/↑/→
